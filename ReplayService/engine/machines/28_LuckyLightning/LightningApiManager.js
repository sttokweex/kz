ApiManager.prototype.GameApi = function (player, prevGameMode, param) {
    var result = {
        balance_bonus: "0",
        balance_cash: "100,000.00",
        balance: "100,000.00",
        bl: "0",
        c: "100.00",
        counter: "1",
        index: "1",
        l: "25",
        na: "s",
        reel_set: "0",
        s: "",
        stime: new Date().getTime(),
        sa: "",
        sb: "",
        sh: "4",
        sver: "5",   
        tw: "0.00",
        w: "0.00",
    };

    //          ,                          
    var screenAbove = Util.view2String(player.machine.virtualReels.above);
    var screenBelow = Util.view2String(player.machine.virtualReels.below);
    result["sa"] = screenAbove;
    result["sb"] = screenBelow;
    result["s"] = Util.view2String(player.machine.view);
    result["c"] = player.betPerLine;
    result["tw"] = player.machine.winMoney;
    result["w"] = player.machine.winMoney;

    //                                 
    var winLines = player.machine.winLines;
    for (var i = 0; i < winLines.length; i++) {
        result[`l${i}`] = winLines[i];
    }
    result["index"] = param.index;
    result["counter"] = ++param.counter;
    //                                           
    var nextAction = "s";
    if (player.machine.winMoney > 0) {
        nextAction = "c";
    }

    result["na"] = nextAction;
    result["balance"] = player.balance;
    result["balance_cash"] = player.balance;

    //             api
    if (player.machine.moneyCache.values.length > 0) {
        result["mo"] = player.machine.moneyCache.values.join();
        result["mo_t"] = player.machine.moneyCache.table.join();
    }

    if (player.machine.prevWildRespin == false && player.machine.wildRespin == true) {
        result["na"] = "s";
        result["rs_c"] = 1;
        result["rs_m"] = 1;
        result["rs_p"] = 0;
        result["rs"] = "mc";

        var sty = [];
        for (var i = 0; i < player.machine.sticky.length; i++) {
            var pos = player.machine.sticky[i];
            sty.push(`${pos},${pos}`);
        }
        if (sty.length > 0) {
            result["sty"] = sty.join('~');
        }

        if (player.machine.maskView.length > 0) {
            result["is"] = Util.view2String(player.machine.maskView);
        }
    } else if (player.machine.prevWildRespin == true && player.machine.wildRespin == false) {
        result["mo_c"] = 1;
        result["mo_tv"] = player.machine.moneyTotalValue;
        result["mo_tw"] = player.machine.moneyTotalValue * player.betPerLine;
        if (player.machine.moneyPositions.length > 0) {
            result["mo_wpos"] = player.machine.moneyPositions.join();
        }
        result["rs_t"] = 1;
        result["reel_set"] = 0;
        result["s"] = Util.view2String(player.machine.wildRespinView);
        result["is"] = Util.view2String(player.machine.view);
        var sty = [];
        for (var i = 0; i < player.machine.sticky.length; i++) {
            var pos = player.machine.sticky[i];
            sty.push(`${pos},-1`);
        }
        if (sty.length > 0) {
            result["sty"] = sty.join('~');
        }
    }
    if (prevGameMode == "BASE") {
        if (player.machine.currentGame == "FREE") {
            result["na"] = "s";
            result["fs"] = 1;
            result["fsmax"] = player.machine.freeSpinLength;
            result["fsmul"] = 1;
            result["fswin"] = "0.00";
            result["fsres"] = "0.00";
            result["reel_set"] = 0;
            result["psym"] = `1~${player.machine.scatterWin}~${player.machine.scatterPositions.join()}`;
            if (player.machine.isBonusBuy) {
                result["puri"] = "0";
                result["purtr"] = "1";
            }
        }
    } else if (prevGameMode == "FREE") {
        //                       
        result["tw"] = player.machine.freeSpinWinMoney;
        result["reel_set"] = 0;
        if (player.machine.isBonusBuy) {
            result["puri"] = "0";
        }

        if (player.machine.isFreeSpinAdd) {
            result["fsmore"] = 10;
        }

        if (player.machine.currentGame == "FREE") {
            result["na"] = "s";
            result["fs"] = player.machine.freeSpinIndex;
            result["fsmax"] = player.machine.freeSpinLength;
            result["fsmul"] = 1;
            result["fswin"] = player.machine.freeSpinWinMoney;
            result["fsres"] = player.machine.freeSpinWinMoney;
        } else if (player.machine.currentGame == "BASE") {
            //                                     ->                       
            result["na"] = "c";
            result["fs_total"] = player.machine.freeSpinLength;
            result["fsend_total"] = 1;
            result["fsmul_total"] = 1;
            result["fswin_total"] = player.machine.freeSpinWinMoney;
            result["fsres_total"] = player.machine.freeSpinWinMoney;
        }
    }

    return result;
}

